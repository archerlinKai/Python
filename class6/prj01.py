# while 迴圈示範
# 指派運算子（縮寫）範例
# +=, -=, *=, /=, //=, %=, **=
# X = X + 1 等同於 X += 1
# X = X - 1 等同於 X -= 1
# X = X * 2 等同於 X *= 2
# X = X / 2 等同於 X /= 2
# X = X // 2 等同於 X //= 2
# X = X % 2 等同於 X %= 2
# X = X ** 2 等同於 X **= 2

# 運算子優先順序（由高到低）
# 1. 括號 ()
# 2. 指數 **
# 3. 正負號 +-
# 4. 乘除取餘 * / // %
# 5. 加減 +-
# 6. 比較運算子 < > <= >= == !=
# 7. 邏輯運算子 and or not
# 8. 指派運算子 =, +=, -=, ...

i = 1
while i < 10:
    print(i)  # 印出目前的 i
    i += 1  # 等同於 i = i + 1

# 密碼檢查範例
user_input = input("請輸入密碼: ")  # 讀取使用者輸入
n = "1031"  # 正確密碼
while user_input != n:  # 當輸入不正確時重複詢問
    print("密碼錯誤，請重新輸入")  # 提示錯誤
    user_input = input("請輸入密碼: ")  # 重新讀取輸入
print("密碼正確")  # 輸入正確，結束
