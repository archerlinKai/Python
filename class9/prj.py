# List 更新資料示範
# List 與一般變數類似，但允許指定索引位置更新元素
L = [1, 2, 3, 4, 5]
L[0] = 10  # 將索引 0 的元素從 1 改為 10
print(L)

# 呼叫方式：call by value（不可變型態示範）
# 將一個不可變的資料型態（如 int）賦值給另一個變數，修改新的變數不會影響原變數
a = 10
b = a  # 將 a 的值賦給 b
b = 20  # 修改 b 的值
print(a)  # a 的值不受影響

# 呼叫方式：call by reference（可變型態示範）
# List 是可變資料型態，直接賦值會傳遞參考位置
L1 = [1, 2, 3]
L2 = L1  # L2 與 L1 指向相同的 list 物件
L2[0] = 10  # 修改 L2 的值會影響到 L1
print(L1)  # L1 的值也會跟著改變

a = 10
# 使用 copy() 複製 list，避免互相影響
L3 = L1.copy()  # 用 copy() 複製 List，產生新的獨立物件
L3[0] = 99
print(L1)  # 原本的 L1 不受 L3 修改影響
print(L3)  # 複製後的 L3 可以獨立修改
