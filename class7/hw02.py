# 飲料點餐示範
# 使用 while 迴圈持續顯示選單，直到使用者選擇離開
while True:
    # 顯示選單項目
    print("1. 蘋果汁")
    print("2. 柳橙汁")
    print("3. 葡萄汁")
    print("4. 系統關閉")

    n = input("請輸入編號:")  # 讀取使用者輸入的選項編號（字串）

    # 根據輸入不同顯示相對應商品，輸入 "4" 時結束程式
    if n == "1":
        print("您點的商品是蘋果汁")  # 回應使用者選擇
    elif n == "2":
        print("您點的商品是柳橙汁")
    elif n == "3":
        print("您點的商品是葡萄汁")
    elif n == "4":
        print("系統關閉")
        break  # 跳出 while 迴圈結束程式
    else:
        print("輸入錯誤，查無此果汁，請重新輸入")  # 非預期輸入時提示
